#ifndef _ARCH_ARMV7M_STM32F4_BITS_STM32F4_SPI_H_
#define _ARCH_ARMV7M_STM32F4_BITS_STM32F4_SPI_H_

//reg offsets
#define STM32F4_REGOFF_SPI_CR1              0x00
#define STM32F4_REGOFF_SPI_CR2              0x04
#define STM32F4_REGOFF_SPI_SR               0x08
#define STM32F4_REGOFF_SPI_DR               0x0C
#define STM32F4_REGOFF_SPI_CRCPR            0x10
#define STM32F4_REGOFF_SPI_RXCRCR           0x14
#define STM32F4_REGOFF_SPI_TXCRCR           0x18
#define STM32F4_REGOFF_SPI_I2SCFGR          0x1C
#define STM32F4_REGOFF_SPI_I2SPR            0x20

//bits values
#define STM32F4_REGMASK_SPI_CR1_BIDIMODE    (1<<15)
#define STM32F4_REGMASK_SPI_CR1_BIDIOE      (1<<14)
#define STM32F4_REGMASK_SPI_CR1_CRCEN       (1<<13)
#define STM32F4_REGMASK_SPI_CR1_CRCNEXT     (1<<12)
#define STM32F4_REGMASK_SPI_CR1_DFF         (1<<11)
#define STM32F4_REGMASK_SPI_CR1_RXONLY      (1<<10)
#define STM32F4_REGMASK_SPI_CR1_SSM         (1<<9)
#define STM32F4_REGMASK_SPI_CR1_SSI         (1<<8)
#define STM32F4_REGMASK_SPI_CR1_LSBFIRST    (1<<7)
#define STM32F4_REGMASK_SPI_CR1_SPE         (1<<6)
#define STM32F4_REGSHIFT_SPI_CR1_BR         3
#define STM32F4_REGMASK_SPI_CR1_MSTR        (1<<2)
#define STM32F4_REGMASK_SPI_CR1_CPOL        (1<<1)
#define STM32F4_REGMASK_SPI_CR1_CPHA        (1<<0)

#define STM32F4_REGMASK_SPI_CR2_TXEIE       (1<<7)
#define STM32F4_REGMASK_SPI_CR2_RXNEIE      (1<<6)
#define STM32F4_REGMASK_SPI_CR2_ERRIE       (1<<5)
#define STM32F4_REGMASK_SPI_CR2_FRF         (1<<4)
#define STM32F4_REGMASK_SPI_CR2_SSOE        (1<<2)
#define STM32F4_REGMASK_SPI_CR2_TXDMAEN     (1<<1)
#define STM32F4_REGMASK_SPI_CR2_RXDMAEN     (1<<0)

#define STM32F4_REGMASK_SPI_SR_FRE          (1<<8)
#define STM32F4_REGMASK_SPI_SR_BSY          (1<<7)
#define STM32F4_REGMASK_SPI_SR_OVR          (1<<6)
#define STM32F4_REGMASK_SPI_SR_MODF         (1<<5)
#define STM32F4_REGMASK_SPI_SR_CRCERR       (1<<4)
#define STM32F4_REGMASK_SPI_SR_UDR          (1<<3)
#define STM32F4_REGMASK_SPI_SR_CHSIDE       (1<<2)
#define STM32F4_REGMASK_SPI_SR_TXE          (1<<1)
#define STM32F4_REGMASK_SPI_SR_RXNE         (1<<0)

#define STM32F4_REGMASK_SPI_I2SCFGR_I2SMOD  (1<<11)
#define STM32F4_REGMASK_SPI_I2SCFGR_I2SE    (1<<10)
#define STM32F4_REGMASK_SPI_I2SCFGR_I2SMAST (1<<9)
#define STM32F4_REGMASK_SPI_I2SCFGR_I2SRX   (1<<8)
#define STM32F4_REGSHIFT_SPI_I2SCFGR_I2SCFG 8
#define STM32F4_REGMASK_SPI_I2SCFGR_PCMSYNC (1<<7)
#define STM32F4_REGSHIFT_SPI_I2SCFGR_I2SSTD 4
#define STM32F4_REGMASK_SPI_I2SCFGR_CKPOL   (1<<5)
#define STM32F4_REGSHIFT_SPI_I2SCFGR_DATLEN 1
#define STM32F4_REGMASK_SPI_I2SCFGR_CHLEN   (1<<0)

#define STM32F4_REGMASK_SPI_I2SPR_MCKOE     (1<<9)
#define STM32F4_REGMASK_SPI_I2SPR_ODD       (1<<8)
#define STM32F4_REGSHIFT_SPI_I2SPR_I2SDIV   0


#endif /* _ARCH_ARMV7M_STM32F4_BITS_STM32F4_SPI_H_ */
